export const searchProperties = (filters: {
  propertyType?: string;
  locationId?: number;
  beds?: number;
  status?: string;
} | string) => {
  if (typeof filters === 'string') {
    return propertiesData.filter(p =>
      p.title.toLowerCase().includes(filters.toLowerCase()) ||
      p.description.toLowerCase().includes(filters.toLowerCase())
    );
  }

  return propertiesData.filter(p => {
    if (filters.propertyType && p.propertyType !== filters.propertyType) return false;
    if (filters.locationId && p.locationId !== filters.locationId) return false;
    if (filters.beds && p.beds !== filters.beds) return false;
    if (filters.status && p.status !== filters.status) return false;
    return true;
  });
};
